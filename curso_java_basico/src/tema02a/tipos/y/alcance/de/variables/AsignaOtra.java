public class AsignaOtra {

	public static void main(String[] args) {
		
		boolean tengoCalor = true;
		char letra0 = 'n';
		char letra1 = 110;
		char letra2 = '\u006E';
		
		System.out.println("\n> > > > > > > > > > Comienza el programa < < < < < < < < < <");
		
		System.out.println("\nEl valor de tengoCalor es: " + tengoCalor);
		
		System.out.println("\nEl valor de letra0 es: " + letra0);
		System.out.println("El valor de letra1 es: " + letra1);
		System.out.println("El valor de letra2 es: " + letra2);
		
		System.out.println("\nEsto es\nun mensaje\nde tres líneas");
			
		System.out.println("\nMe llamo \"Jose\"");
		System.out.println("\nMe llamo \\Jose\\");
		
		System.out.println("\n> > > > > > > > > > Finaliza el programa < < < < < < < < < <");
	}

}
